  public int[][] getTestData1_processingTime(int numberOfJob, int randomSeed, int range, int numberOfMachine){
    int processingTime[][] = new int[numberOfMachine][numberOfJob];
    java.util.Random r = new java.util.Random(randomSeed);
    for(int i = 0 ; i < numberOfMachine ; i ++ ){
      for(int j = 0 ; j < numberOfJob ; j ++ ){
        processingTime[i][j] = 1+(int)(r.nextDouble()*range);
      }
    }
    return processingTime;
  }

